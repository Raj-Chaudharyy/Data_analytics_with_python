from sklearn.impute import SimpleImputer
import pandas as pd
import numpy as np

df=pd.read_csv('/content/googleplaystore (1).csv')

df

df.isnull().sum()

imputer=SimpleImputer(missing_values=np.nan,strategy='most_frequent')

df.iloc[:,2:3]=imputer.fit_transform(df.iloc[:,2:3])

df.isnull().sum()

df=df.dropna()
